#version 430

// --------------------------------------------
// input vertex data
// --------------------------------------------
in vec3 fTexCoord;

// --------------------------------------------
// output fragment color
// --------------------------------------------
out vec4 FragColor;

// --------------------------------------------
// uniform
// --------------------------------------------
uniform samplerCube uSkybox;

// --------------------------------------------
// main function
// --------------------------------------------
void main()
{
    FragColor = texture(uSkybox, fTexCoord);
}